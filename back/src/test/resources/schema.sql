DROP TABLE IF EXISTS comments;
DROP TABLE IF EXISTS posts;
DROP TABLE IF EXISTS subscription;
DROP TABLE IF EXISTS topics;
DROP TABLE IF EXISTS users;

CREATE TABLE users (
  user_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  created_at TIMESTAMP,
  email VARCHAR(50) NOT NULL UNIQUE,
  password VARCHAR(255) NOT NULL,
  updated_at TIMESTAMP,
  username VARCHAR(50) NOT NULL UNIQUE
);

CREATE TABLE topics (
  topic_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(255) NOT NULL,
  description VARCHAR(255) NOT NULL
);

CREATE TABLE posts (
  post_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  topic_id BIGINT,
  content VARCHAR(255) NOT NULL,
  created_at DATE NOT NULL,
  title VARCHAR(255) NOT NULL,
  user_id BIGINT,
  CONSTRAINT FK_post_user FOREIGN KEY (user_id) REFERENCES users(user_id),
  CONSTRAINT FK_post_topic FOREIGN KEY (topic_id) REFERENCES topics(topic_id)
);

CREATE TABLE comments (
  comment_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  content VARCHAR(255) NOT NULL,
  created_at DATE NOT NULL,
  user_id BIGINT NOT NULL,
  post_id BIGINT NOT NULL,
  CONSTRAINT FK_comment_user FOREIGN KEY (user_id) REFERENCES users(user_id),
  CONSTRAINT FK_comment_post FOREIGN KEY (post_id) REFERENCES posts(post_id)
);

CREATE TABLE subscription (
  topic_id BIGINT NOT NULL,
  user_id BIGINT NOT NULL,
  PRIMARY KEY (topic_id, user_id),
  CONSTRAINT FK_sub_topic FOREIGN KEY (topic_id) REFERENCES topics(topic_id) ON DELETE CASCADE,
  CONSTRAINT FK_sub_user FOREIGN KEY (user_id) REFERENCES users(user_id) ON DELETE CASCADE
);